export const LIBRARY_SYMBOLS = Object.keys(FONT_5X7);

export function stateFromSymbol(symbol) {
	const normalized = String(symbol).toUpperCase();
	const pattern = FONT_5X7[normalized] || FONT_5X7.A;
	const state = createBlankState();
	const top = 3;
	const left = 4;
	for (let row = 0; row < pattern.length; row += 1) {
		for (let col = 0; col < pattern[row].length; col += 1) {
			if (pattern[row][col] === "1") {
				state[indexFromCell(top + row, left + col)] = 1;
			}
		}
	}
	return state;
}

export function createDemoState() {
	const state = stateFromSymbol("M");
	for (let offset = 0; offset < 13; offset += 2) {
		state[indexFromCell(offset, offset)] = 1;
		state[indexFromCell(offset, 12 - offset)] = 1;
	}
	return state;
}

